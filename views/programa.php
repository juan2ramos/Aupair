                <h2>Escuela <span class="bold color-blue">Au pair.</span></h2>
                <hr/>
                <p>

                    El programa está enfocado en fortalecer tú perfil ocupacional y cultural, así como tú proceso de
                    documentación y aplicación al programa; te asesoramos y acompañamos en cada uno de los momentos
                    relacionados con el mismo como: formación lingüística, documentación y tramite de visado,
                    (asesoramiento para la presentación ante la embajada con altas probabilidades porcentuales en la
                    aprobación de la visa previniendo resultados negativos), atención especializada y confiable para ti
                    durante tu proceso de ubicación en Colombia y durante tu estadía en el exterior ya que también
                    contarás con acompañamiento en tu país de destino a través de nuestras agencias representantes.
                </p>

