                de edad, solteros y sin hijos; el cual consiste en vivir, trabajar y estudiar un año en países como
                Alemania, Francia, Estados Unidos ó China, en calidad de hermana mayor, con una familia anfitriona en
                donde su principal labor será el cuidado de los niños de la familia.
